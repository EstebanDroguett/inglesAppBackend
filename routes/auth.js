//------------------------------------------------------------------------------------3----------------------------------------------------------------------------------
const { Router } = require('express');
//------------------------------------------------------------------------------------3----------------------------------------------------------------------------------
//------------------------------------------------------------------------------------5----------------------------------------------------------------------------------
const { check } = require('express-validator');
//------------------------------------------------------------------------------------5----------------------------------------------------------------------------------

//------------------------------------------------------------------------------------4----------------------------------------------------------------------------------
const { registerUser, loginUser, revalidateToken } = require('../controllers/auth');
//------------------------------------------------------------------------------------4----------------------------------------------------------------------------------
//------------------------------------------------------------------------------------5----------------------------------------------------------------------------------
const { validateFields } = require('../middelwares/validate-fields');
//------------------------------------------------------------------------------------5----------------------------------------------------------------------------------
//------------------------------------------------------------------------------------12----------------------------------------------------------------------------------
const { validateJWT } = require('../middelwares/validate-jwt');
//------------------------------------------------------------------------------------12----------------------------------------------------------------------------------

//------------------------------------------------------------------------------------3----------------------------------------------------------------------------------
const router = Router();


router.post('/register',
    [//Middelwares
        check('name', 'El nombre es obligatorio.').not().isEmpty(),
        check('lastName', 'El apellido es obligatorio.').not().isEmpty(),
        check('email', 'El email es obligatorio.').isEmail(),
        check('password', 'La contraseña debe ser de 6 caracteres.').isLength({ min: 6 }),
        validateFields
    ],
    registerUser);

router.post('/',
    [//Middelwares
        check('email', 'El email es obligatorio.').isEmail(),
        check('password', 'La contraseña debe ser de 6 caracteres.').isLength({ min: 6 }),
        validateFields
    ],
    loginUser);

router.get('/revalidateToken', validateJWT, revalidateToken);

module.exports = router;
